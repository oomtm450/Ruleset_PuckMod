        internal PausableTimer Timer { get; set; }

        internal bool CurrentPenalty { get; set; }

        internal DateTime PenaltyDateTime { get; set; }

        internal PlayerTeam Team { get; set; }

        internal string Position { get; set; }

        internal string PlayerNumber { get; set; }

        internal string PlayerUsername { get; set; }

        internal string ReceivingPlayerSteamId { get; set; }

        internal Penalty(string steamId, PlayerTeam team, string playerNumber, string playerUsername, PenaltyType penaltyType, string position, string receivingPlayerSteamId) {
            SteamId = steamId;
            Team = team;
            PlayerNumber = playerNumber;
            PlayerUsername = playerUsername;
            PenaltyType = penaltyType;
            Position = position;
            CurrentPenalty = false;
            ReceivingPlayerSteamId = receivingPlayerSteamId;

            SetTimer();

            PenaltyDateTime = DateTime.UtcNow;
        }

        internal void SetTimer() {
            Timer = new PausableTimer(PenaltyTimer_Elapsed, PenaltyModule.GetPenaltyTypeTime(PenaltyType));
        }

        private void PenaltyTimer_Elapsed() {
            try {
                if (!PenaltyModule.PenalizedPlayers.ContainsKey(SteamId))
                    return;

                Ruleset.PenaltyTimersElapsed.Add(this);
            }
            catch (Exception ex) {
                Logging.LogError($"Error in {nameof(PenaltyTimer_Elapsed)}.\n{ex}", Ruleset.ServerConfig);
            }
        }

        internal static void PenaltyTimer_Action(Penalty penaltyToRemove) {
            try {
                if (penaltyToRemove == null)
                    return;

                PenaltyModule.PenalizedPlayers[penaltyToRemove.SteamId].Remove(penaltyToRemove);

                string penaltyOverMsg = $"PENALTY #{penaltyToRemove.PlayerNumber} {penaltyToRemove.PlayerUsername} OVER";
                Ruleset.SystemChatMessages.Add(penaltyOverMsg);
                Logging.Log(penaltyOverMsg, Ruleset.ServerConfig);

                // Unpenalize player if no more penalties or start the next one.
                if (PenaltyModule.PenalizedPlayers[penaltyToRemove.SteamId].Count == 0)
                    PenaltyModule.UnpenalizePlayer(penaltyToRemove.SteamId, penaltyToRemove.Team, penaltyToRemove.Position);
                else {
                    Penalty firstPenalty = PenaltyModule.PenalizedPlayers[penaltyToRemove.SteamId].First();
                    firstPenalty.CurrentPenalty = true;
                }

                PenaltyModule.UnpausePenalties();
                if (GameManager.Instance.Phase != GamePhase.Play)
                    PenaltyModule.PausePenalties();
            }
            catch (Exception ex) {
                Logging.LogError($"Error in {nameof(PenaltyTimer_Action)}.\n{ex}", Ruleset.ServerConfig);
            }
        }
    }

    public enum PenaltyType {
        Interference,
        [Description("Goalie interference"), Category("ToString")]
        GoalieInterference,
        [Description("Delay of game"), Category("ToString")]
        DelayOfGame,
        Tripping,
        Embellishment,
        [Description("Faceoff violation"), Category("ToString")]
        FaceoffViolation,
        Roughing,
        Charging,
    }
